#[cfg(test)]
mod test_performance {

    use zench::bench;
    use zench::bx;

    // ----------------------------------------------------------------
    // VEC CLONE BENCHMARK
    // ----------------------------------------------------------------
    // Measures Vec<u8> clone performance for varying sizes.
    // 1 - Allocate new memory
    // 2 - Copy all bytes
    // 3 - Create a new Vec
    // ----------------------------------------------------------------

    #[rustfmt::skip]
    const CASES: &[i32] = &[
        0,
        100,
        1000,
        10_000,
        100_000,
        500_000,
        1_000_000,
    ];

    #[test]
    fn benchmark_clone() {
        let mut b = bench!();

        for &size in CASES {
            // generate cases Vec<random u8>
            let original: Vec<u8> = (0..size)
                .map(|i| (i * 31 + 17) as u8) // rand 1 byte
                .collect();

            b.bench(size, || {
                bx(original.to_vec());
            });
        }
    }
}

