#[cfg(test)]
mod tests {

    use zench::bench;
    use zench::bx;

    #[test]
    fn bench_bx_vs_black_box() {
        bench!(

            // ----------------------------------------------------------------
            // Baseline overhead of the benchmark
            "empty" => {},


            // ----------------------------------------------------------------
            // 'bx' and 'std::hint::black_box' have the same execution time,
            "bx" => bx(""),
            "black_box" =>  std::hint::black_box(""),
            // ----------------------------------------------------------------
            "bx x2" => {
                bx(bx(""));
                bx(bx(""));
            }
            ,
            "black_box x2" => {
                bx(std::hint::black_box(""));
                bx(std::hint::black_box(""));
            },
            // ----------------------------------------------------------------
            "bx x3" => {
                bx(bx(""));
                bx(bx(""));
                bx(bx(""));
            },
            "black_box x3" => {
                bx(std::hint::black_box(""));
                bx(std::hint::black_box(""));
                bx(std::hint::black_box(""));
            },
            // ----------------------------------------------------------------
            "bx x4" => {
                bx(bx(""));
                bx(bx(""));
                bx(bx(""));
                bx(bx(""));
            },

            "black_box x4" => {
                 bx(std::hint::black_box(""));
                 bx(std::hint::black_box(""));
                 bx(std::hint::black_box(""));
                 bx(std::hint::black_box(""));
            },
        )
        .report(|r| {
            let (mut empty, mut rest) = r
                .filter_n(1)
                .split();

            empty
                .title("Empty - just self overhead")
                .print();

            for n in 1..=4 {
                let (mut group, next) = rest
                    .filter_n(2)
                    .split();

                group
                    .title(format!("{n} barrier"))
                    .print();

                rest = next;
            }

            rest.print();
        });
    }
}

